package beans;

public class Itens {
    private int id_ped;
    private int id_pro;
    private int num_tens;

    public Itens() {
    }

    public int getId_ped() {
        return id_ped;
    }

    public void setId_ped(int id_ped) {
        this.id_ped = id_ped;
    }

    public int getId_pro() {
        return id_pro;
    }

    public void setId_pro(int id_pro) {
        this.id_pro = id_pro;
    }

    public int getNum_tens() {
        return num_tens;
    }

    public void setNum_tens(int num_tens) {
        this.num_tens = num_tens;
    }
    
    
}
